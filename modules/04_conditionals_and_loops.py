# === CONDITIONALS ===

age = 20

if age >= 18:
    print("You're an adult.")
else:
    print("You're a minor.")

# === LOOPS ===

print("Counting with for loop:")
for i in range(5):
    print(i)

print("Counting with while loop:")
count = 0
while count < 3:
    print(count)
    count += 1

